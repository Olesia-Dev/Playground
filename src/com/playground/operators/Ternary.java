package com.playground.operators;

public class Ternary {

    public static void main(String[] args) {
        int n1 = 27, n2 = 31, max;
        System.out.println("First number: " + n1);
        System.out.println("Second number: " + n2);

        max = Math.max(n1, n2);
        System.out.println("Maximum is = " + max);
    }

}
